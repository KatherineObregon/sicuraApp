package com.example.sicuraapp.Entities;

public class Coordenadas {
    Double origenLat;
    Double origenLng;
    Double destinoLat;
    Double destinoLong;

    public Double getOrigenLat() {
        return origenLat;
    }

    public void setOrigenLat(Double origenLat) {
        this.origenLat = origenLat;
    }

    public Double getOrigenLng() {
        return origenLng;
    }

    public void setOrigenLng(Double origenLng) {
        this.origenLng = origenLng;
    }

    public Double getDestinoLat() {
        return destinoLat;
    }

    public void setDestinoLat(Double destinoLat) {
        this.destinoLat = destinoLat;
    }

    public Double getDestinoLong() {
        return destinoLong;
    }

    public void setDestinoLong(Double destinoLong) {
        this.destinoLong = destinoLong;
    }
}
